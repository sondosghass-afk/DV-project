import pandas as pd
import plotly.express as px
from dash import Dash, dcc, html, Input, Output

df = pd.read_csv("/content/retail_store_sales.csv")

df["Transaction Date"] = pd.to_datetime(df["Transaction Date"], errors="coerce", dayfirst=True)
df["Year"] = df["Transaction Date"].dt.year

app = Dash(__name__)

app.layout = html.Div([
    html.H2("Retail Store Sales Dashboard"),

    html.Label("Select Category:"),
    dcc.Dropdown(
        id="category",
        options=[{"label": c, "value": c} for c in sorted(df["Category"].dropna().unique())],
        value=sorted(df["Category"].dropna().unique())[0],
        clearable=False
    ),

    html.Label("Select Year:"),
    dcc.Slider(
        id="year",
        min=int(df["Year"].min()),
        max=int(df["Year"].max()),
        value=int(df["Year"].min()),
        marks={int(y): str(int(y)) for y in sorted(df["Year"].dropna().unique())}
    ),

    dcc.Graph(id="chart1"),
    dcc.Graph(id="chart2")
])

@app.callback(
    [Output("chart1", "figure"),
     Output("chart2", "figure")],
    [Input("category", "value"),
     Input("year", "value")]
)
def update_dashboard(category, year):
    dff = df[(df["Category"] == category) & (df["Year"] == year)]

    fig1 = px.bar(
        dff,
        x="Item",
        y="Total Spent",
        color="Payment Method",
        title="Total Sales by Item",
        hover_data=["Quantity", "Price Per Unit", "Location"]
    )

    fig2 = px.scatter(
        dff,
        x="Price Per Unit",
        y="Total Spent",
        size="Quantity",
        color="Payment Method",
        title="Price Per Unit vs Total Spent",
        hover_data=["Item", "Quantity", "Location"]
    )

    return fig1, fig2

app.run(jupyter_mode="inline", debug=False)

